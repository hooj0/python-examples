#===============================================================================
# 示例：多变量赋值
#===============================================================================
# 描述：唯一的前提就是变量的数量必须跟序列元素的数量是一样的
#-------------------------------------------------------------------------------


#-------------------------------------------------------------------------------
### tuple
#-------------------------------------------------------------------------------
data = ('a', 2, ('b', 10.2))

a, b, c = data
print('a: ', a) # a
print('b: ', b) # 2
print('c: ', c) # ('b', 10.2)
print()


#-------------------------------------------------------------------------------
###list
#-------------------------------------------------------------------------------
data = [ 'a', 2, ('c', 2.2) ]

a, b, c = data
print('a: ', a) # a
print('b: ', b) # 2
print('c: ', c) # ('b', 10.2)
print()


#-------------------------------------------------------------------------------
###进一步赋值
#-------------------------------------------------------------------------------
a, b, (c, d) = data
print('a: ', a) # a
print('b: ', b) # 2
print('c: ', c) # c
print('d: ', d) # 2.2
print()


#-------------------------------------------------------------------------------
### 字符串赋值
#-------------------------------------------------------------------------------
data = '1234'

a, b, c, d = data
print('a: ', a) # 1
print('b: ', b) # 2
print('c: ', c) # 3
print('d: ', d) # 4
print()

#-------------------------------------------------------------------------------
### 占位符赋值；解压一部分，丢弃其他的值
#-------------------------------------------------------------------------------
data = [ 'ACME', 50, 91.1, (2012, 12, 21) ]
_, a, b, _ = data
print('a: ', a) # 50
print('b: ', b) # 91.1


#-------------------------------------------------------------------------------
#    当赋值的变量数量比被赋值的对象数据长度多，将发生异常
#-------------------------------------------------------------------------------
a, b, c = (2, 3)
print('a: ', a)
print('b: ', b)
print('c: ', c) # ValueError: not enough values to unpack (expected 3, got 2)

